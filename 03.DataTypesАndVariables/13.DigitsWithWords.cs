using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace _13.DigitsWithWords
{
    class Program
    {
        static void Main(string[] args)
        {
            string digitAsWord = Console.ReadLine();

            if (digitAsWord == "zero")
            {
                Console.WriteLine("0");
            }
            else if (digitAsWord == "one")
            {
                Console.WriteLine("1");
            }
            else if (digitAsWord == "two")
            {
                Console.WriteLine("2");
            }
            else if (digitAsWord == "three")
            {
                Console.WriteLine("3");
            }
            else if (digitAsWord == "four")
            {
                Console.WriteLine("4");
            }
            else if (digitAsWord == "five")
            {
                Console.WriteLine("5");
            }
            else if (digitAsWord == "six")
            {
                Console.WriteLine("6");
            }
            else if (digitAsWord == "seven")
            {
                Console.WriteLine("7");
            }
            else if (digitAsWord == "eight")
            {
                Console.WriteLine("8");
            }
            else if (digitAsWord == "nine")
            {
                Console.WriteLine("9");
            }
        }
    }
}
